// Math.floor () : [floor => Lower value]

// Take the value (10.1). the Math.floor () is take the previous / lower immediate value i.e. 10.
console.log (Math.floor (10.1));
// Output => 10


// Here we take a variable i & give them a value 2.5, the Math.floor () is take the previous / lower  
// immediate value i.e. 2.
var i = 2.5;
console.log (Math.floor (i));
// Output => 2
