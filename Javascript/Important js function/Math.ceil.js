// Math.ceil () : [ceiling => Top of the value]

// Take the value (10.1). the Math.ceil () is take the next immediate top value i.e. 11.
console.log (Math.ceil (10.1));
// Output => 11


// Here we take a variable i & give them a value 2.5, the Math.ceil () is take the next 
// immediate top value i.e. 3.
var i = 2.5;
console.log (Math.ceil (i));
// Output => 3
