// Arithmatic Operators
// (Addition, Substraction, Division, Multiplication)

let num1 = 20;
let num2 = 5;
console.log (num1 + num2); // returns 25. 
console.log (num1 - num2); // returns 15.
console.log (num1 / num2); // returns 4.
console.log (num1 * num2); // returns 100.

// Modulus / Reminder (%)
// also called 'Mod'
let a = 10;
let b = a%2;
console.log (b); // returns 0.
// this program express like this-
// a = 10;
// b = a%2;
// b = 10%2; // put the value of a & it divide by 2, then the division is 5 and the reminder   is 0, because for 5 there is nothing remain after divide by 2.
// b = 0


// IMPORTANT
// %2 === 0 (it is even, means any value divide by 2 & reminder is 0 then it is even.)
// %2 !== 0 (it is odd, means any value divide by 2 & reminder is not 0 then it is odd.)