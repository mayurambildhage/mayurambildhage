// Postfix decreament (a--)
let a = 2;
console.log (a--); // returns 2
console.log (a);   // returns 1
console.log (a--); // returns 1
console.log (a);   // returns 0

// Example of Postfix (a--) :
let c = 10;
console.log (c--); // returns same value i.e. 10
console.log (c);   // returns decremented value i.e. 9
console.log (c--); // returns letest value of c i.e. 9
console.log (c--); // returns decremented value i.e. 8


// Prefix decrement (--b)
let b = 2;
console.log (--b); // returns 1
console.log (b);   // returns 1
console.log (--b); // returns 0
console.log (--b); // returns -1

// Example of Prefix (--b) :
let d = 10;
console.log (--d); // returns decremented value i.e. 9
console.log (d);   // returns letest value i.e. 9
console.log (--d); // returns decremented value i.e. 8
console.log (--d); // returns decremented value i.e. 7
