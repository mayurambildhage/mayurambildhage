// Postfix increament (a++)
let a = 1;
console.log (a++); // returns 1
console.log (a);   // returns 2
console.log (a++); // returns 2
console.log (a++); // returns 3

// Example of Postfix (a++) :
 let c = 10;
 console.log (c++); // returns same value i.e. 10
 console.log (c);   // returns incremented value i.e. 11
 console.log (c++); // returns letest value of c i.e. 11
 console.log (c++); // returns incremented value i.e. 12


// Prefix increment(++b)
let b = 1;
console.log (++b); // returns 2
console.log (b);   // returns 2
console.log (++b); // returns 3
console.log (++b); // returns 4

// Example of Prefix (++b) :
let d = 10;
console.log (++d); // returns incremented value i.e. 11
console.log (d);   // returns letest value i.e. 11
console.log (++d); // returns incremented value i.e. 12
console.log (++d); // returns incremented value i.e. 13
