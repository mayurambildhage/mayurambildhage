// Relational Operators
// (>,<,>=,<=)
// in this the relation is express in boolean value i.e. 'true' or 'false'.

let num1 = 20;
let num2 = 5;
console.log (num1 > num2); // returns true, means 20 is greter than 5.
console.log (num1 < num2); // returns false, means 20 is less than 5.
console.log (num1 >= num2); // retuns true, means 20 is greater than equals to 5.
console.log (num1 <= num2); // returns false, means 20 is less than equals to 5.