// Assignment Operators
// (+=, -=, /=, *=)

let num1 = 20;
console.log (num1 += 10); // returns 30, adds 10 to 20.(now num1 = 30)
console.log (num1 -= 10); // returns 20, substract 10 from 30. (now num1 = 20)
console.log (num1 /= 5);  // returns 4, divide 20 by 5. (now num1 = 4)
console.log (num1 *= 5);  // returns 20, multiplies 5 with 4. (now num1 = 20)